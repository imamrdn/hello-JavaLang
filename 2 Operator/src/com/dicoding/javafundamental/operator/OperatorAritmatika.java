package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {
    public static void main(String[] args){
        System.out.println("Operator Penjumlahan");
        int hasilPenjumlahan = 5+1;
        System.out.println("Hasil 5 + 1 adalah "+ hasilPenjumlahan);
        System.out.println();

        System.out.println("Operator Pengurangan");
        int hasilPengurangan = 4- 1;
        System.out.println("Hasil dari 4 - 1 adalah "+ hasilPengurangan);
        System.out.println();

        System.out.println("Operator Pembagian");
        int hasilPembagian = 20/2;
        System.out.println("Hasil dari 20 / 4 adalah "+ hasilPembagian);
        System.out.println();
    }
}
